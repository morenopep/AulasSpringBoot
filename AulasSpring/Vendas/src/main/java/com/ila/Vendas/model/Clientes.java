package com.ila.Vendas.model;

public class Clientes {
	//Atributos
	private String nome;
	private int cpf;
	
	//metodos Construtor
	
	public Clientes() {
		this.nome = nome;
		this.cpf  = cpf;
	}

	//METODOS DE ACESSO
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getCpf() {
		return cpf;
	}

	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	
	
}
